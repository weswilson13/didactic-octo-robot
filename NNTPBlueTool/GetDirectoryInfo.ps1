[CmdletBinding()]
param(
    [string]$Domain="dc03.mydomain.local",

    [ValidateSet('GetUsers','GetOUs','ViewUser')]
    [string]$Action = 'ViewUser',

    [string]$Username,

    [adsi]$DirectoryEntry,

    [switch]$uID
)

Add-Type -AssemblyName System.DirectoryServices

$filter = switch ($Action) {
    'GetUsers' { "(&(objectCategory=person)(objectClass=user))"; break }
    'GetOUs'   { "(objectClass=organizationalUnit)"; break }
    'ViewUser' { "(sAMAccountName=$Username)"; break }
    default   { throw "Invalid action specified. Use 'GetUsers', 'GetOUs', or 'ViewUser'." }
}

if ($PSBoundParameters.ContainsKey('DirectoryEntry')) {
    $_directoryEntry = $DirectoryEntry
}
else {
    $_directoryEntry = [adsi]::new("LDAP://$Domain")
}
try {
    $result = [adsisearcher]::new($_directoryEntry,$filter).FindAll()
}
catch {
    throw "Failed to connect to the directory. Please check the domain and your network connection.`n$(@{Domain = $Domain;Action=$Action;SearchFilter=$filter} | Out-String)"
    return
}
if ($Action -in 'GetUsers','GetOUs') {
    try {
        $result = $result | Out-GridView -PassThru -Title "Select an entry"
    }
    catch {
        throw $error[0]
    }
}
elseif ($Action -eq 'ViewUser') {
    try { 
        $result.Properties | Out-GridView -Wait -Title "User Details - $($result.Properties.samaccountname)"
        return
    }
    catch {
        throw $error[0]
    }
}

if ($result) {
    $entry = $result.GetDirectoryEntry()

    $output = switch ($Action) {
        'GetUsers' { if ($uID.IsPresent) { $entry.uid } else { $entry.sAMAccountName } }
        'GetOUs'   { $entry.distinguishedName }
    }
    return $output
}

return

